"""
investigation/nodes/report.py
===============================
LangGraph node: Report

Drafts a formal bilingual SAR (Suspicious Activity Report) in:
  • English  — FinCEN Form 111 structure (US standard)
  • French   — TRACFIN CERFA 10534 structure (French FIU standard)

Two-Pass Generation Strategy
-------------------------------
  Pass 1 (English): Full SAR narrative generated with all context.
  Pass 2 (French):  English narrative is translated + adapted for TRACFIN
                    structure, NOT independently re-generated.
                    This avoids hallucination divergence between versions.

Self-Critique Guard (NeMo Guardrails)
--------------------------------------
  After each generation pass, the output rails check that:
  • No legal statute is cited that isn't in the approved whitelist.
  • No hallucinated transaction amounts (all amounts must appear in the
    context; the guardrail runs a regex comparison against known values).
  • The SAR has mandatory fields: subject, activity description,
    reporting entity, narrative, and law enforcement contact fields.
  If any check fails, the node returns with `sar_report.status = DRAFT`
  and adds the violation to `guardrail_violations` for analyst review.

SAR Structure (FinCEN Form 111 alignment)
------------------------------------------
  Part I   — Subject Information (from KYC chunks)
  Part II  — Suspicious Activity Information (typology, dates, amounts)
  Part III — Narrative (free-text, generated by LLM)
  Part IV  — Filing Institution Information (static config)
"""

