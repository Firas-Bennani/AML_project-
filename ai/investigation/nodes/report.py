# ---------------------------------------------------------------------------
# Report node
# ---------------------------------------------------------------------------

class ReportNode:
    """
    LangGraph node: Generates bilingual SAR (EN + FR) using NIM LLM.

    Two-pass generation with NeMo Guardrails on both passes.
    """

    def __init__(
        self,
        llm: Any,                     # LangChain BaseChatModel
        guardrails: Optional[Any],    # nemoguardrails.LLMRails or None
        reporting_institution: str = "AML Compliance Dept.",
        max_evidence_chars: int = 2000,
    ) -> None:
        self.llm = llm
        self.guardrails = guardrails
        self.reporting_institution = reporting_institution
        self.max_evidence_chars = max_evidence_chars

    # ------------------------------------------------------------------
    # LangGraph callable
    # ------------------------------------------------------------------

    async def __call__(self, state: AMLState) -> Dict[str, Any]:
        """Generate bilingual SAR report and return updated state."""
        node_id = state["node_id"]
        logger.info("[report] Drafting bilingual SAR for node=%s", node_id)

        # Pass 1: English narrative
        en_prompt = self._build_prompt_en(state)
        narrative_en, violations_en = await self._generate(
            en_prompt, SAR_SYSTEM_PROMPT_EN, lang="EN"
        )

        # Pass 2: French narrative (translation of English + TRACFIN adaptation)
        fr_prompt = self._build_prompt_fr(state, narrative_en)
        narrative_fr, violations_fr = await self._generate(
            fr_prompt, SAR_SYSTEM_PROMPT_FR, lang="FR"
        )

        all_violations = violations_en + violations_fr
        status = "DRAFT" if all_violations else "PENDING_REVIEW"

        sar: SARReport = {
            "report_id": f"SAR-{uuid.uuid4().hex[:12].upper()}",
            "node_id":   node_id,
            "risk_score": state["risk_score"],
            "typologies": [
                t for t, s in state["typology_scores"].items() if s >= 0.4
            ],
            "narrative_en":  narrative_en,
            "narrative_fr":  narrative_fr,
            "evidence_refs": self._collect_evidence_refs(state),
            "analyst_notes": state.get("human_feedback") or "",
            "status":        status,
        }

        logger.info(
            "[report] SAR %s generated | status=%s | violations=%d",
            sar["report_id"], status, len(all_violations),
        )

        return {
            "sar_report": sar,
            "guardrail_violations": all_violations,
            "messages": [
                {
                    "role": "assistant",
                    "content": (
                        f"[report] SAR {sar['report_id']} drafted. "
                        f"Status: {status}. Violations: {len(all_violations)}."
                    ),
                }
            ],
        }

    # ------------------------------------------------------------------
    # Prompt builders
    # ------------------------------------------------------------------

    def _build_prompt_en(self, state: AMLState) -> str:
        typologies = [t for t, s in state["typology_scores"].items() if s >= 0.4]
        return SAR_HUMAN_TEMPLATE_EN.format(
            node_id=state["node_id"],
            risk_score=state["risk_score"],
            risk_level=state.get("risk_level", "HIGH"),
            risk_verdict=state.get("risk_verdict", "VERIFIED"),
            typologies=", ".join(typologies) or "Unknown",
            rule_hits=", ".join(state.get("rule_hits", [])) or "None identified",
            risk_rationale=state.get("risk_rationale", "")[:800],
            kyc_evidence=self._format_chunks(state.get("kyc_chunks", []))[: self.max_evidence_chars],
            news_evidence=self._format_chunks(state.get("news_chunks", []))[: self.max_evidence_chars],
            graph_summary=state.get("graph_summary", "")[:500],
            similar_sars=self._format_sars(state.get("similar_sars", []))[:1000],
        )

    def _build_prompt_fr(self, state: AMLState, english_narrative: str) -> str:
        typologies = [t for t, s in state["typology_scores"].items() if s >= 0.4]
        return SAR_HUMAN_TEMPLATE_FR.format(
            node_id=state["node_id"],
            risk_score=state["risk_score"],
            risk_level=state.get("risk_level", "HIGH"),
            risk_verdict=state.get("risk_verdict", "VERIFIED"),
            typologies=", ".join(typologies) or "Inconnu",
            risk_rationale=state.get("risk_rationale", "")[:600],
            kyc_evidence=self._format_chunks(state.get("kyc_chunks", []))[: self.max_evidence_chars],
            news_evidence=self._format_chunks(state.get("news_chunks", []))[: self.max_evidence_chars],
            graph_summary=state.get("graph_summary", "")[:400],
            english_narrative=english_narrative[:1200],
        )

    # ------------------------------------------------------------------
    # LLM generation with Guardrails
    # ------------------------------------------------------------------

    async def _generate(
        self,
        human_prompt: str,
        system_prompt: str,
        lang: str,
    ):
        """Run one generation pass through NeMo Guardrails + LLM."""
        messages = [
            {"role": "system", "content": system_prompt},
            {"role": "user",   "content": human_prompt},
        ]
        violations: List[str] = []

        if self.guardrails is not None:
            try:
                text = await self.guardrails.generate_async(messages=messages)
            except Exception as exc:
                v = f"[{lang}] Guardrail violation: {exc}"
                logger.warning("[report] %s", v)
                violations.append(v)
                text = f"[SAR DRAFT BLOCKED BY GUARDRAIL — {lang}]\n{exc}"
        else:
            resp = await self.llm.ainvoke(messages)
            text = resp.content if hasattr(resp, "content") else str(resp)

        # Post-generation statute validation (lightweight regex guard)
        statute_violations = self._check_hallucinated_statutes(text, lang)
        violations.extend(statute_violations)

        return text, violations

    # ------------------------------------------------------------------
    # Statute hallucination check
    # ------------------------------------------------------------------

    # Approved statute patterns (extend this whitelist as needed)
    _APPROVED_STATUTES = re.compile(
        r"31\s+CFR\s+§?1020|BSA|31\s+USC\s+§?5318|"
        r"FATF\s+Rec|L561-\d+|AMLD[56]|Directive\s+201[58]/\d+|"
        r"Wolfsberg|Basel\s+AML",
        re.IGNORECASE,
    )
    # Pattern to catch things that LOOK like statute references
    _STATUTE_PATTERN = re.compile(
        r"\b(?:§|Art\.|Article|Section|Sec\.|Regulation|Directive|CFR|USC|CMF)\s*[\d\-\.]+",
        re.IGNORECASE,
    )

    def _check_hallucinated_statutes(self, text: str, lang: str) -> List[str]:
        """
        Find all statute-like references in the text and flag any
        that do NOT match the approved whitelist pattern.
        """
        violations = []
        for match in self._STATUTE_PATTERN.finditer(text):
            citation = match.group(0)
            if not self._APPROVED_STATUTES.search(citation):
                v = f"[{lang}] Potential hallucinated statute: {citation!r}"
                logger.warning("[report] %s", v)
                violations.append(v)
        return violations

    # ------------------------------------------------------------------
    # Evidence helpers
    # ------------------------------------------------------------------

    @staticmethod
    def _format_chunks(chunks: list) -> str:
        if not chunks:
            return "No documents available."
        return "\n\n".join(
            f"[{i+1}] (score={c['score']:.3f}) {c['content'][:400]}"
            for i, c in enumerate(chunks)
        )

    @staticmethod
    def _format_sars(sars: list) -> str:
        if not sars:
            return "No similar cases found."
        return "\n\n".join(
            f"[Case {i+1} | typology={c['metadata'].get('typology','?')}]\n"
            f"{c['content'][:400]}"
            for i, c in enumerate(sars)
        )

    @staticmethod
    def _collect_evidence_refs(state: AMLState) -> List[str]:
        """Collect doc_ids from all retrieved chunks as evidence references."""
        refs = []
        for chunk_list in [
            state.get("kyc_chunks", []),
            state.get("news_chunks", []),
            state.get("similar_sars", []),
        ]:
            refs.extend(c["doc_id"] for c in chunk_list)
        return list(dict.fromkeys(refs))  # deduplicate, preserve order
